package com.park.einvoice.h5.domain;

/**
 * 申请开票返回信息的bean
 * @author WangYuefei
 *
 */
public class AppInvoice{
	private String code,msg;
	private AppInvoiceData data;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public AppInvoiceData getData() {
		return data;
	}
	public void setData(AppInvoiceData data) {
		this.data = data;
	}
	@Override
	public String toString() {
		return "HasInvoice [code=" + code + ", msg=" + msg + ", data=" + data + "]";
	}
}
